static void render_gumbo( Renderer& renderer );

void render_gumbo_example()
{
    const vec3 light_position( 64.0f, 128.0f, -64.0f );

    Options options;
    options.set_resolution( 512, 512, 1.0f );
    options.set_near_clip_distance( 4.0f );
    options.set_far_clip_distance( 512.0f );

    Renderer renderer;
    renderer.set_options( options );
    renderer.begin();
    renderer.two_sided( true );
    renderer.shading_rate( 1.0f );
    renderer.perspective( float(M_PI) / 12.0f );
    renderer.projection();
    renderer.look_at( vec3(-12.0f, 0.0f, 24.0f), light_position, vec3(0.0f, 1.0f, 0.0f) );
    renderer.begin_world();
    renderer.rotate( 0.5f * float(M_PI), 1.0f, 0.0f, 0.0f );
    renderer.rotate( 1.0f * float(M_PI), 0.0f, 1.0f, 0.0f );
    renderer.rotate( 0.25f * float(M_PI), 0.0f, 0.0f, 1.0f );
    render_gumbo( renderer );
    renderer.end_world();
    renderer.end();
    renderer.shadow_from_framebuffer( "shadow_map" );

    options.set_resolution( 640, 480, 1.0f );
    options.set_dither( 2.0f );
    options.set_gain( 1.5f );
    options.set_gamma( 1.25f );
    options.set_filter( &Options::gaussian_filter, 2.0f, 2.0f );

    renderer.set_options( options );
    renderer.begin();
    renderer.two_sided( true );
    renderer.perspective( float(M_PI) / 2.0f );
    renderer.projection();
    renderer.translate( 3.0f, -12.0f, 12.0f );
    renderer.begin_world();

    Grid& ambientlight = renderer.light_shader( "../shaders/ambientlight.sl" );
    ambientlight["intensity"] = 0.3f;
    ambientlight["lightcolor"] = vec3( 1.0f, 1.0f, 1.0f );

    Grid& shadowpointlight = renderer.light_shader( "../shaders/shadowpointlight.sl" );
    shadowpointlight["intensity"] = 16384.0f;
    shadowpointlight["lightcolor"] = vec3( 1.0f, 1.0f, 1.0f );
    shadowpointlight["from"] = light_position;
    shadowpointlight["shadowname"] = "shadow_map";
    shadowpointlight["bias"] = 0.05f;

    renderer.push_attributes();
    renderer.rotate( 0.5f * float(M_PI), 1.0f, 0.0f, 0.0f );
    renderer.rotate( 1.0f * float(M_PI), 0.0f, 1.0f, 0.0f );
    renderer.rotate( 0.25f * float(M_PI), 0.0f, 0.0f, 1.0f );
    render_gumbo( renderer );
    renderer.pop_attributes();
    
    const vec3 P0( -64.0f, 0.0f, 64.0f );
    const vec3 P1( 64.0f, 0.0f, 64.0f );
    const vec3 P2( 64.0f, 0.0f, -64.0f );
    const vec3 P3( -64.0f, 0.0f, -64.0f );
    const vec3 N( 0.0f, 1.0f, 0.0f );
    const vec2 T0( 0.0f, 1.0f );    
    const vec2 T1( 1.0f, 1.0f );    
    const vec2 T2( 1.0f, 0.0f );    
    const vec2 T3( 0.0f, 0.0f );    
    renderer.identity();
    renderer.color( vec3(0.6f, 0.6f, 0.6f) );
    renderer.surface_shader( "../shaders/matte.sl" );
    vec3 positions[] = { P0, P1, P2, P3 };
    vec3 normals[] = { N, N, N, N };
    vec2 texture_coordinates[] = { T0, T1, T2, T3 };
    renderer.polygon( 4, positions, normals, texture_coordinates );

    renderer.end_world();
    renderer.end();
    renderer.save_image_as_png( "gumbo.png" );
}

